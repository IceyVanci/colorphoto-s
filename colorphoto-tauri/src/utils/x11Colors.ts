interface X11Color {
  hex: string;
  en: string;
  cn: string;
  r: number;
  g: number;
  b: number;
}

function hexToRgb(hex: string): { r: number; g: number; b: number } | null {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result ? {
    r: parseInt(result[1], 16),
    g: parseInt(result[2], 16),
    b: parseInt(result[3], 16)
  } : null;
}

function colorDistance(r1: number, g1: number, b1: number, r2: number, g2: number, b2: number): number {
  return Math.sqrt(
    Math.pow(r1 - r2, 2) +
    Math.pow(g1 - g2, 2) +
    Math.pow(b1 - b2, 2)
  );
}

function findClosestColor(hex: string): X11Color | null {
  const rgb = hexToRgb(hex);
  if (!rgb) return null;
  
  let closest = X11_COLORS[0];
  let minDistance = Infinity;
  
  for (const color of X11_COLORS) {
    const distance = colorDistance(rgb.r, rgb.g, rgb.b, color.r, color.g, color.b);
    if (distance < minDistance) {
      minDistance = distance;
      closest = color;
    }
  }
  
  return closest;
}

export function getColorName(hex: string, language: 'cn' | 'en' = 'cn'): string {
  const closest = findClosestColor(hex);
  if (!closest) return '';
  return language === 'en' ? closest.en : closest.cn;
}

